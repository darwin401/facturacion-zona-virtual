  saveInvoice(data: Bill) {
    const billings = this.getBills();
    if (billings) {
      billings.push(data);
      localStorage.setItem('billings', JSON.stringify(billings));
    }
  }

  validateExisting(Trans_codigo: string): boolean {
    const billings = this.getBills();
    return billings.some((bill: Bill) => bill.Trans_codigo === Trans_codigo);
  }

  getBills(): Bill[] {
    return JSON.parse(localStorage.getItem('billings')!);
  }

  getBillById(idComercio: number): Bill {
    const billings : Bill[] = this.getBills();

    const response = billings.find(
      (element) => element.comercio_codigo === idComercio
    );
    return response!;
  }

  updateBills( tradeId: number, data: Bill ) {

    let billings: Bill[] = this.getBills();

    const indiceActualizar = billings.findIndex(
      (bill : Bill) => bill.comercio_codigo === tradeId
    );

    // Verificar si se encontró el registro
    if (indiceActualizar !== -1) {
      billings[indiceActualizar].comercio_nombre = data.comercio_nombre;
      billings[indiceActualizar].comercio_nit = data.comercio_nit;
      billings[indiceActualizar].comercio_direccion = data.comercio_direccion;
      billings[indiceActualizar].Trans_medio_pago = data.Trans_medio_pago;
      billings[indiceActualizar].Trans_estado = data.Trans_estado;
      billings[indiceActualizar].Trans_total = data.Trans_total;
      billings[indiceActualizar].Trans_fecha = data.Trans_fecha;
      billings[indiceActualizar].Trans_concepto = data.Trans_concepto;
      billings[indiceActualizar].usuario_identificacion = data.usuario_identificacion;
      billings[indiceActualizar].usuario_nombre = data.usuario_nombre;
      billings[indiceActualizar].usuario_email = data.usuario_email;

      localStorage.setItem('billings', JSON.stringify(billings));
    }
  }
